// Типы для TypeScript
export type SloganCategory = keyof typeof BOSS_AI_SLOGANS;

// Функция для получения случайного слогана
export const getRandomSlogan = (): string => {
    const categories = Object.values(BOSS_AI_SLOGANS);
    const allSlogans = categories.flat();
    return allSlogans[Math.floor(Math.random() * allSlogans.length)];
};

// Функция для получения слогана по категории
export const getSloganByCategory = (category: SloganCategory): string => {
    const slogans = BOSS_AI_SLOGANS[category];
    return slogans[Math.floor(Math.random() * slogans.length)];
};

// Функция для получения всех слоганов категории
export const getAllSlogansByCategory = (category: SloganCategory): string[] => {
    return BOSS_AI_SLOGANS[category];
};

// Функция для получения слогана по индексу
export const getSloganByIndex = (category: SloganCategory, index: number): string => {
    const slogans = BOSS_AI_SLOGANS[category];
    return slogans[index % slogans.length];
};

// Функция для получения случайного слогана из случайной категории
export const getRandomSloganFromRandomCategory = (): { slogan: string; category: SloganCategory } => {
    const categories = Object.keys(BOSS_AI_SLOGANS) as SloganCategory[];
    const randomCategory = categories[Math.floor(Math.random() * categories.length)];
    const slogan = getSloganByCategory(randomCategory);
    return { slogan, category: randomCategory };
};

// Функция для поиска слоганов по ключевому слову
export const searchSlogans = (keyword: string): Array<{ slogan: string; category: SloganCategory }> => {
    const results: Array<{ slogan: string; category: SloganCategory }> = [];
    const lowerKeyword = keyword.toLowerCase();

    Object.entries(BOSS_AI_SLOGANS).forEach(([category, slogans]) => {
        slogans.forEach(slogan => {
            if (slogan.toLowerCase().includes(lowerKeyword)) {
                results.push({ slogan, category: category as SloganCategory });
            }
        });
    });

    return results;
};

// Функция для получения статистики слоганов
export const getSlogansStats = () => {
    const totalSlogans = Object.values(BOSS_AI_SLOGANS).flat().length;
    const categoryStats = Object.entries(BOSS_AI_SLOGANS).map(([category, slogans]) => ({
        category,
        count: slogans.length,
        percentage: Math.round((slogans.length / totalSlogans) * 100)
    }));

    return {
        total: totalSlogans,
        categories: categoryStats
    };
};

